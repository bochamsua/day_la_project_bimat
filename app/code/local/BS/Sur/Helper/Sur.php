/**
 * Ncr helper
 *
 * @category    BS
 * @package     BS_Ncr
 * @author Bui Phong
 */
class BS_Sur_Helper_Sur extends Mage_Core_Helper_Abstract
{

    /**
     * get base files dir
     *
     * @access public
     * @return string
     * @author Bui Phong
     */
    public function getFileBaseDir()
    {
        return Mage::getBaseDir('media').DS.'sur'.DS.'file';
    }

    /**
     * get base file url
     *
     * @access public
     * @return string
     * @author Bui Phong
     */
    public function getFileBaseUrl()
    {
        return Mage::getBaseUrl('media').'sur'.'/'.'file';
    }
}
